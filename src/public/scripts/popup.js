/* Cocinas */

var open_coc1 = document.getElementById('open_coc1'),
    cocina1 = document.getElementById('cocina1'),
    popup_cocina1 = document.getElementById('popup_cocina1'),
    btn_cerrar_popup = document.getElementById('btn_cerrar_popup');

open_coc1.addEventListener('click',function(){
    cocina1.classList.add('active');
});
btn_cerrar_popup.addEventListener('click',function(){
    cocina1.classList.remove('active');
});
